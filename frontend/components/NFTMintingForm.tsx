export default function NFTMintingForm() {
  const [formData, setFormData] = useState({
    recipient: '',
    image: null as File | null,
    requiredSignatures: 1,
    useCustomValidators: false,
    customValidators: [] as string[],
    isPFP: false,
    price: '',
    mintType: 0,
  });

  const [loading, setLoading] = useState(false);

  const handleImageUpload = (e: React.ChangeEvent<HTMLInputElement>) => {
    const file = e.target.files?.[0];
    if (file) {
      setFormData({ ...formData, image: file });
    }
  };

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setLoading(true);

    try {
      if (!window.ethereum) throw new Error("Please install MetaMask");
      if (!formData.image) throw new Error("Please select an image");

      // Validate recipient address
      if (formData.mintType === 0 || formData.mintType === 1) {
        if (!formData.recipient) throw new Error("Recipient address is required");
        if (!ethers.isAddress(formData.recipient)) throw new Error("Invalid recipient address");
      }

      // Validate price
      if (!formData.price) throw new Error("Price is required");

      // Upload image to IPFS via Pinata
      const ipfsHash = await uploadToPinata(formData.image);

      // Create metadata and upload to IPFS
      const metadata = {
        name: "MultiSig NFT",
        description: "A multi-signature validated NFT",
        image: `ipfs://${ipfsHash}`,
        attributes: []
      };

      const metadataIpfsHash = await uploadToPinata(
        new Blob([JSON.stringify(metadata)], { type: 'application/json' })
      );

      // Connect to contract and mint NFT
      const provider = new ethers.BrowserProvider(window.ethereum);
      const signer = await provider.getSigner();
      const contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, signer);

      const tx = await contract.createNFT(
        formData.recipient || ethers.ZeroAddress, // Use ZeroAddress if no recipient is specified
        metadataIpfsHash,
        formData.requiredSignatures,
        formData.useCustomValidators,
        formData.customValidators,
        formData.isPFP,
        ethers.parseEther(formData.price).toString(), // Convert to string
        formData.mintType
      );

      await tx.wait();
      alert("NFT created successfully!");
    } catch (error) {
      console.error('Error creating NFT:', error);
      alert(`Failed to create NFT: ${error instanceof Error ? error.message : 'Unknown error'}`);
    } finally {
      setLoading(false);
    }
  };

  return (
    <form onSubmit={handleSubmit} className="space-y-6">
      <div>
        <Label>Mint Type</Label>
        <RadioGroup
          defaultValue="0"
          onValueChange={(value) => 
            setFormData({ ...formData, mintType: parseInt(value) })
          }
          className="flex space-x-4"
        >
          <div className="flex items-center space-x-2">
            <RadioGroupItem value="0" id="direct" />
            <Label htmlFor="direct">Direct Mint</Label>
          </div>
          <div className="flex items-center space-x-2">
            <RadioGroupItem value="1" id="designated" />
            <Label htmlFor="designated">Designated Recipient</Label>
          </div>
          <div className="flex items-center space-x-2">
            <RadioGroupItem value="2" id="marketplace" />
            <Label htmlFor="marketplace">Marketplace Listing</Label>
          </div>
        </RadioGroup>
      </div>

      {(formData.mintType === 0 || formData.mintType === 1) && (
        <div>
          <Label htmlFor="recipient">Recipient Address</Label>
          <Input
            id="recipient"
            value={formData.recipient}
            onChange={(e) => 
              setFormData({ ...formData, recipient: e.target.value })
            }
            placeholder="Enter recipient wallet address"
          />
        </div>
      )}

      <div>
        <Label htmlFor="image">NFT Image</Label>
        <div className="mt-1 flex items-center">
          <Input
            id="image"
            type="file"
            accept="image/*"
            onChange={handleImageUpload}
            className="hidden"
          />
          <Button
            type="button"
            onClick={() => document.getElementById('image')?.click()}
            variant="outline"
            className="w-full"
          >
            <Upload className="mr-2 h-4 w-4" />
            {formData.image ? formData.image.name : 'Upload Image'}
          </Button>
        </div>
      </div>

      <div>
        <Label htmlFor="signatures">Required Signatures</Label>
        <Input
          id="signatures"
          type="number"
          min="1"
          value={formData.requiredSignatures}
          onChange={(e) => 
            setFormData({ 
              ...formData, 
              requiredSignatures: parseInt(e.target.value) 
            })
          }
        />
      </div>

      <div>
        <Label htmlFor="price">Price (ETH)</Label>
        <Input
          id="price"
          type="number"
          step="0.001"
          value={formData.price}
          onChange={(e) => setFormData({ ...formData, price: e.target.value })}
          placeholder="Enter price in ETH"
        />
      </div>

      <Button type="submit" className="w-full" disabled={loading}>
        {loading ? 'Creating NFT...' : 'Create NFT'}
      </Button>
    </form>
  );
}

